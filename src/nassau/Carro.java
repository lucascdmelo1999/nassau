package nassau;


public class Carro {
	
	private String cor;
	private String modelo;
	
	
	public Carro(String cor, String modelo) {
		this.cor = cor;
		this.modelo = modelo;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getmodelo() {
		return modelo;
	}
	public void setmodelo(String modelo) {
		this.modelo = modelo;
	};
	
	

}
